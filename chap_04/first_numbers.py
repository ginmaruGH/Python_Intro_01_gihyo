# 数値のリストを作成する
print("\n数値のリストを作成する\n")

print("range(1, 6)")
for value in range(1, 6):
    print(value)

print("range(6)")
for value in range(6):
    print(value)

# range()関数を使用して数値リストを作成する
print("\nrange()関数を使用して数値リストを作成する\n")

print("numbers = list(range(1, 6))")
numbers = list(range(1, 6))
print(numbers)
